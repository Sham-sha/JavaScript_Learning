// Math.abs() - absolute value 

console.log(Math.abs(-20))

// Math.sign() - for minus value 

console.log(Math.sign(10))

// Math.sqrt()- for find the sqr route 

console.log(Math.sqrt(16))

// Math.cbrt() - cube route 

console.log(Math.cbrt(8))


// Math.pow(base , exponent)
console.log(Math.pow(10, 18))


// Math.min() for find minimum value 
// Math.max() for find max value 
let arr = [10, 20, 30, 1, 60, 70]

console.log(Math.min(...arr)+Math.max(...arr))

// Math.random() to generate random number 
// ToFixed 

let randomNum = Math.random()*100000
console.log(randomNum.toFixed(0))

// Math.ceil() up

console.log(Math.ceil(1.1))

// Math.floor() down

console.log(Math.floor(2.99))

// Math.round() round .5 => up 

console.log(Math.round(3.5))


// Math.trunc() remove decimal 
console.log(Math.trunc(10.99999999999))

